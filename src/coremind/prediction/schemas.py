"""Prediction schemas for the predictive memory layer.

Defines the :class:`Prediction` model that represents falsifiable predictions
about near-future state, along with supporting types for evidence and verification.
"""

from __future__ import annotations

from datetime import UTC, datetime
from typing import Literal

from pydantic import BaseModel, ConfigDict, Field

type PredictionStatus = Literal["pending", "verified", "falsified", "expired"]


class Prediction(BaseModel):
    """A falsifiable prediction about near-future state.

    Predictions are generated by the PredictiveMemory layer based on
    temporal patterns observed in the world. Each prediction includes:
    - A hypothesis about what will happen
    - A confidence score (0-1)
    - Evidence points supporting the prediction
    - Verification criteria for falsifiability
    - A status tracking whether it was verified or falsified

    All text fields (description, verification_criteria) MUST be in French.
    """

    model_config = ConfigDict(frozen=True)

    id: str = Field(min_length=1)
    created_at: datetime = Field(default_factory=lambda: datetime.now(UTC))
    domain: str = Field(
        min_length=1, description="Domain of the prediction (e.g., 'sleep', 'health', 'home')"
    )
    description: str = Field(min_length=1, description="French description of the prediction")
    confidence: float = Field(ge=0.0, le=1.0, description="Confidence score (0-1)")
    evidence_points: list[str] = Field(
        default_factory=list, description="Evidence supporting the prediction"
    )
    verification_criteria: str = Field(
        min_length=1, description="French criteria for verifying/falsifying the prediction"
    )
    status: PredictionStatus = Field(
        default="pending", description="Current status of the prediction"
    )
    horizon_hours: int = Field(ge=1, description="Time horizon in hours")
    verified_at: datetime | None = Field(
        default=None, description="Timestamp when prediction was verified/falsified"
    )


class PredictionEvidence(BaseModel):
    """Evidence supporting a prediction.

    Represents a single piece of evidence that contributed to a prediction's
    confidence score.
    """

    model_config = ConfigDict(frozen=True)

    observation: str = Field(min_length=1, description="The observation that serves as evidence")
    similarity_score: float = Field(ge=0.0, le=1.0, description="Similarity to current observation")
    timestamp: datetime = Field(default_factory=lambda: datetime.now(UTC))
